
# set union and insection

set1 = {'blue', 'orange','pink', 'yellow'}
set2 = {'brown', 'black', 'white', 'yellow', 'blue'}

print("set1 union set2")
print(set1.union(set2))

print("set1 intersection set2")
print(set1.intersection(set2))

print("set1 difference set2")
print(set1.difference(set2))
