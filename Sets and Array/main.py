# set operation 

#integer set
s1 = {1,2,0,10,7,9}
print(s1)



s2 = {10,20,'codingal',(100,200)}
print(s2)

#set does not have duplicate values
s3 = {1,2,3,4,5,3,3,4}
print(s3)

#set from a list
s4 = set([10,20,30,90,77,89])
print(s4)

#add elements
s4.add(999)
print(s4)
#remove the element
s4.pop()
print(s4)